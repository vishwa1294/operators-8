class ArithmeticOperatorsDemo{
public static void main(String[]args){
int a=10;
int b=20;
System.out.println("a:"+a);
System.out.println("b:"+b);
int addResult=a+b;
System.out.println("a+b:"+addResult);
int subResult=b-a;
System.out.println("b-a:"+subResult);
int mulResult=a*b;
System.out.println("a*b:"+mulResult);
int divResult=b/a;
System.out.println("b/a:"+divResult);
int moduloResult=b%a;
System.out.println("b%a:"+moduloResult);
}
}

